// Helper to convert Gemini payload to OpenAI / Groq / OpenRouter Messages format
function convertToOpenAIMessages(systemInstruction: string, formattedContents: any[]) {
  const messages: any[] = [];
  if (systemInstruction) {
    messages.push({ role: "system", content: systemInstruction });
  }

  for (const item of formattedContents) {
    const role = item.role === "model" ? "assistant" : "user";
    const contentParts: any[] = [];

    if (Array.isArray(item.parts)) {
      for (const p of item.parts) {
        if (p.text) {
          contentParts.push({ type: "text", text: p.text });
        } else if (p.inlineData) {
          contentParts.push({
            type: "image_url",
            image_url: {
              url: `data:${p.inlineData.mimeType};base64,${p.inlineData.data}`
            }
          });
        }
      }
    }

    if (contentParts.length === 1 && contentParts[0].type === "text") {
      messages.push({ role, content: contentParts[0].text });
    } else if (contentParts.length > 0) {
      messages.push({ role, content: contentParts });
    }
  }

  return messages;
}

// Helper to convert Gemini FunctionDeclarations to OpenAI / Groq / OpenRouter Tools format
function convertToOpenAITools(geminiTools: any[]) {
  const openAITools: any[] = [];
  for (const tool of geminiTools) {
    if (tool.functionDeclarations) {
      for (const fd of tool.functionDeclarations) {
        const convertProperties = (props: any): any => {
          if (!props) return undefined;
          const newProps: any = {};
          for (const key of Object.keys(props)) {
            const prop = props[key];
            newProps[key] = {
              type: String(prop.type || "string").toLowerCase(),
              description: prop.description,
            };
            if (prop.properties) {
              newProps[key].properties = convertProperties(prop.properties);
            }
          }
          return newProps;
        };

        openAITools.push({
          type: "function",
          function: {
            name: fd.name,
            description: fd.description,
            parameters: {
              type: "object",
              properties: convertProperties(fd.parameters?.properties),
              required: fd.parameters?.required
            }
          }
        });
      }
    }
  }
  return openAITools.length > 0 ? openAITools : undefined;
}


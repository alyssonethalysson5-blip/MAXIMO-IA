// Endpoint to export the current project workspace as a ZIP file (excluding node_modules, dist, etc.)
app.get("/api/export-project", async (req, res) => {
  try {
    const zip = new JSZip();
    const rootPath = process.cwd();

    function addFilesToZip(dirPath: string, zipFolder: JSZip) {
      const items = fs.readdirSync(dirPath);
      for (const item of items) {
        const fullPath = path.join(dirPath, item);
        const stat = fs.statSync(fullPath);

        // Exclude large binary modules and build directories
        if (
          item === "node_modules" ||
          item === "dist" ||
          item === ".git" ||
          item === "temp" ||
          item === ".next" ||
          item === "build" ||
          item === "coverage" ||
          item === ".cache"
        ) {
          continue;
        }

        if (stat.isDirectory()) {
          const newFolder = zipFolder.folder(item);
          if (newFolder) {
            addFilesToZip(fullPath, newFolder);
          }
        } else {
          const content = fs.readFileSync(fullPath);
          zipFolder.file(item, content);
        }
      }
    }

    addFilesToZip(rootPath, zip);

    const zipBuffer = await zip.generateAsync({ type: "nodebuffer" });

    res.setHeader("Content-Disposition", "attachment; filename=projeto_completo.zip");
    res.setHeader("Content-Type", "application/zip");
    res.send(zipBuffer);
  } catch (error: any) {
    console.error("Erro ao gerar ZIP do projeto:", error);
    res.status(500).json({ success: false, error: error.message });
  }
});

// Start server and handle Vite middleware
async function startServer() {
  if (process.env.NODE_ENV !== "production") {
    const vite = await createViteServer({
      server: { middlewareMode: true },
      appType: "spa",
    });
    app.use(vite.middlewares);
  } else {
    const distPath = path.join(process.cwd(), "dist");
    app.use(express.static(distPath));
    app.get("*", (req, res) => {
      res.sendFile(path.join(distPath, "index.html"));
    });
  }

  app.listen(PORT, "0.0.0.0", () => {
    console.log(`Servidor rodando na porta ${PORT}`);
  });
}

startServer();

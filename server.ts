// API Endpoint to verify and test all configured API Keys
function fetchWithTimeout(url: string, options: any, timeoutMs = 5000): Promise<Response> {
  return Promise.race([
    fetch(url, options),
    new Promise<Response>((_, reject) => setTimeout(() => reject(new Error("Timeout de conexão")), timeoutMs))
  ]);
}

